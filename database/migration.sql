create or replace function public.calculate_booking_price_db(
  p_booking_date date,
  p_start_time time without time zone,
  p_end_time time without time zone
)
returns table (total numeric, dp numeric)
language plpgsql
immutable
set search_path = ''
as $$
declare
  v_day int := extract(dow from p_booking_date);
  v_start int := extract(hour from p_start_time);
  v_end int := extract(hour from p_end_time);
  v_total numeric := 0;
  v_slot record;
  v_price numeric;
begin
  if extract(minute from p_start_time) <> 0 or extract(minute from p_end_time) <> 0 or v_start >= v_end then
    raise exception 'InvalidBookingSlot: invalid start/end time';
  end if;

  for v_slot in
    select * from (values
      (6, 8, null::numeric, null::numeric, 2600000::numeric),
      (8, 10, 1300000::numeric, null::numeric, 2600000::numeric),
      (10, 12, 800000::numeric, null::numeric, 1500000::numeric),
      (12, 14, 800000::numeric, null::numeric, 1500000::numeric),
      (14, 16, 1300000::numeric, 1300000::numeric, 2600000::numeric),
      (16, 18, 2000000::numeric, 2300000::numeric, 2600000::numeric),
      (18, 20, 2300000::numeric, 2500000::numeric, 2800000::numeric),
      (20, 22, 2300000::numeric, 2500000::numeric, 2800000::numeric)
    ) as slots(start_hour, end_hour, weekday_price, friday_price, weekend_price)
  loop
    if v_slot.start_hour >= v_start and v_slot.end_hour <= v_end then
      if v_day between 1 and 4 then
        v_price := coalesce(v_slot.weekday_price, v_slot.weekend_price);
      elsif v_day = 5 then
        v_price := coalesce(v_slot.friday_price, v_slot.weekend_price);
      else
        v_price := v_slot.weekend_price;
      end if;
      v_total := v_total + v_price;
    end if;
  end loop;

  if v_total <= 0 then
    raise exception 'InvalidBookingSlot: no matching price slot';
  end if;

  total := v_total;
  dp := greatest(v_total * 0.3, 500000);
  return next;
end;
$$;

create or replace function public.prevent_customer_booking_update_tampering()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if (select auth.uid()) is not null
    and old.user_id = (select auth.uid())
    and not public.is_admin()
  then
    if new.id is distinct from old.id
      or new.user_id is distinct from old.user_id
      or new.field_id is distinct from old.field_id
      or new.booking_date is distinct from old.booking_date
      or new.start_time is distinct from old.start_time
      or new.end_time is distinct from old.end_time
      or new.price is distinct from old.price
      or new.dp_amount is distinct from old.dp_amount
      or new.created_at is distinct from old.created_at
    then
      raise exception 'ForbiddenBookingMutation: booking details cannot be changed by customers';
    end if;

    if old.status = 'dp_paid' and new.status = 'payment_2_pending' then
      return new;
    end if;

    if old.status = 'payment_2_pending' and new.status = 'dp_paid' then
      if exists (
        select 1 from public.payments p
        where p.booking_id = old.id and p.payment_type = 'final' and p.status = 'pending'
      ) then
        raise exception 'ForbiddenBookingMutation: cannot revert after final payment is recorded';
      end if;
      return new;
    end if;

    raise exception 'ForbiddenBookingMutation: invalid customer status transition';
  end if;

  return new;
end;
$$;

drop trigger if exists prevent_customer_booking_update_tampering on public.bookings;
create trigger prevent_customer_booking_update_tampering
  before update on public.bookings
  for each row execute function public.prevent_customer_booking_update_tampering();
revoke execute on function public.prevent_customer_booking_update_tampering() from anon, authenticated, public;

