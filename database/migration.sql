-- -----------------------------------------------------------------------------
-- 4. Helper: is_admin() — SECURITY DEFINER lookup used by RLS policies to
--    avoid recursive RLS evaluation on public.profiles.
-- -----------------------------------------------------------------------------
create or replace function public.is_admin()
returns boolean
language sql
security definer
set search_path = ''
stable
as $$
  select exists (
    select 1 from public.profiles
    where id = (select auth.uid()) and role = 'admin'
  );
$$;

