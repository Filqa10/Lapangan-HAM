-- -----------------------------------------------------------------------------
-- 6. Lock down SECURITY DEFINER / trigger functions
--    Trigger functions still fire regardless of EXECUTE grants; they must not
--    be exposed as PostgREST RPCs. is_admin() runs inside RLS policies as the
--    policy owner, so it needs no public RPC surface.
-- -----------------------------------------------------------------------------
revoke execute on function public.handle_new_user() from anon, authenticated, public;
revoke execute on function public.prevent_double_booking() from anon, authenticated, public;
revoke execute on function public.set_updated_at() from anon, authenticated, public;
revoke execute on function public.is_admin() from anon, public;
grant execute on function public.is_admin() to authenticated;

-- -----------------------------------------------------------------------------
-- 7. Concurrency-safe double-booking prevention (defense in depth)
--    The prevent_double_booking trigger guards sequential inserts, but a
--    check-then-insert has a race window under concurrent transactions. A GiST
--    EXCLUDE constraint closes it atomically at the schema level.
-- -----------------------------------------------------------------------------
create extension if not exists btree_gist with schema extensions;

-- Generated half-open [start, end) timestamp range from booking_date +
-- start_time/end_time (the spec's columns are left unchanged).
alter table public.bookings
  add column if not exists slot_range tsrange
  generated always as (
    tsrange(
      (booking_date + start_time)::timestamp,
      (booking_date + end_time)::timestamp,
      '[)'
    )
  ) stored;

-- No two non-cancelled bookings for the same field may overlap. Adjacent slots
-- (08-10, 10-12) do not overlap under half-open semantics.
alter table public.bookings drop constraint if exists bookings_no_overlap;
alter table public.bookings
  add constraint bookings_no_overlap
  exclude using gist (
    field_id with =,
    slot_range with &&
  ) where (status <> 'cancelled');

-- -----------------------------------------------------------------------------
-- 8. Admin transactional booking operations
--    These RPCs keep booking/payment status changes atomic for admin approvals.
--    They are SECURITY INVOKER functions, check public.is_admin(), and run as a
--    single Postgres transaction when called through Supabase RPC.
-- -----------------------------------------------------------------------------
create or replace function public.admin_approve_dp(p_booking_id bigint)
returns table (booking_id bigint, user_id uuid)
language plpgsql
set search_path = ''
as $$
declare
  v_booking public.bookings%rowtype;
  v_payment_id bigint;
begin
  if not public.is_admin() then
    raise exception 'Forbidden: admin role required';
  end if;

  select * into v_booking
  from public.bookings
  where id = p_booking_id and status = 'pending'
  for update;

  if not found then
    raise exception 'BookingNotPending: booking is not pending or does not exist';
  end if;

  select p.id into v_payment_id
  from public.payments as p
  where p.booking_id = p_booking_id
    and p.payment_type = 'dp'
    and p.status = 'pending'
  order by p.created_at desc
  limit 1
  for update;

  if v_payment_id is null then
    raise exception 'PendingDPPaymentNotFound: no pending DP payment found';
  end if;

  update public.payments set status = 'approved' where id = v_payment_id;
  update public.bookings set status = 'dp_paid' where id = p_booking_id;

  return query select v_booking.id, v_booking.user_id;
end;
$$;

create or replace function public.admin_approve_final_payment(p_booking_id bigint)
returns table (booking_id bigint, user_id uuid)
language plpgsql
set search_path = ''
as $$
declare
  v_booking public.bookings%rowtype;
  v_payment_id bigint;
begin
  if not public.is_admin() then
    raise exception 'Forbidden: admin role required';
  end if;

  select * into v_booking
  from public.bookings
  where id = p_booking_id and status = 'payment_2_pending'
  for update;

  if not found then
    raise exception 'BookingFinalPaymentNotPending: booking is not waiting for final payment or does not exist';
  end if;

  select p.id into v_payment_id
  from public.payments as p
  where p.booking_id = p_booking_id
    and p.payment_type = 'final'
    and p.status = 'pending'
  order by p.created_at desc
  limit 1
  for update;

  if v_payment_id is null then
    raise exception 'PendingFinalPaymentNotFound: no pending final payment found';
  end if;

  update public.payments set status = 'approved' where id = v_payment_id;
  update public.bookings set status = 'confirmed' where id = p_booking_id;

  return query select v_booking.id, v_booking.user_id;
end;
$$;

create or replace function public.admin_cancel_booking(p_booking_id bigint)
returns table (booking_id bigint, previous_status public.booking_status)
language plpgsql
set search_path = ''
as $$
declare
  v_booking public.bookings%rowtype;
begin
  if not public.is_admin() then
    raise exception 'Forbidden: admin role required';
  end if;

  select * into v_booking
  from public.bookings
  where id = p_booking_id and status <> 'cancelled'
  for update;

  if not found then
    raise exception 'BookingAlreadyCancelledOrMissing: booking is cancelled or does not exist';
  end if;

  update public.bookings set status = 'cancelled' where id = p_booking_id;
  update public.payments as p
  set status = 'rejected'
  where p.booking_id = p_booking_id and p.status = 'pending';

  return query select v_booking.id, v_booking.status;
end;
$$;

grant execute on function public.admin_approve_dp(bigint) to authenticated;
grant execute on function public.admin_approve_final_payment(bigint) to authenticated;
grant execute on function public.admin_cancel_booking(bigint) to authenticated;
revoke execute on function public.admin_approve_dp(bigint) from anon;
revoke execute on function public.admin_approve_final_payment(bigint) from anon;
revoke execute on function public.admin_cancel_booking(bigint) from anon;

