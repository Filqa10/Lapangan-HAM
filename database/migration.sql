-- 3A. handle_new_user — populate public.profiles when an auth user is created.
--     SECURITY DEFINER with a pinned, empty search_path to prevent search-path
--     injection (Supabase security best practice). All object references are
--     fully schema-qualified.
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  insert into public.profiles (id, name, email, phone, role)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'name', 'User'),
    new.email,
    new.raw_user_meta_data ->> 'phone',
    'customer'
  );
  return new;
end;
$$;

update public.profiles as p
set email = u.email
from auth.users as u
where p.id = u.id
  and u.email is not null
  and p.email is distinct from u.email;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

-- 3B. prevent_double_booking — reject overlapping, non-cancelled reservations
--     for the same field/date at the database level. OVERLAPS treats time
--     ranges as half-open [start, end), so adjacent slots (e.g. 08-10 and
--     10-12) do not conflict.
create or replace function public.prevent_double_booking()
returns trigger
language plpgsql
set search_path = ''
as $$
declare
  conflict_count int;
begin
  if new.status = 'cancelled' then
    return new;
  end if;

  select count(*) into conflict_count
  from public.bookings
  where field_id = new.field_id
    and booking_date = new.booking_date
    and status <> 'cancelled'
    and id <> coalesce(new.id, -1)
    and (new.start_time, new.end_time) overlaps (start_time, end_time);

  if conflict_count > 0 then
    raise exception 'DoubleBookingException: Slot waktu pada tanggal ini telah dibooking oleh pelanggan lain.';
  end if;

  return new;
end;
$$;

drop trigger if exists before_booking_upsert on public.bookings;
create trigger before_booking_upsert
  before insert or update on public.bookings
  for each row execute function public.prevent_double_booking();

