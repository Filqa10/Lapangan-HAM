-- 2A. profiles — synced 1:1 with auth.users via on_auth_user_created trigger.
create table if not exists public.profiles (
  id          uuid primary key references auth.users on delete cascade,
  name        text not null,
  email       text,
  phone       text,
  role        public.user_role not null default 'customer',
  created_at  timestamptz not null default timezone('utc'::text, now())
);

alter table public.profiles
  add column if not exists email text;

-- 2B. fields — bookable venues.
create table if not exists public.fields (
  id          bigint generated by default as identity primary key,
  name        text not null,
  price       numeric(10, 2) not null,
  address     text,
  status      text not null default 'active' check (status in ('active', 'inactive')),
  created_at  timestamptz not null default timezone('utc'::text, now()),
  updated_at  timestamptz not null default timezone('utc'::text, now())
);

-- 2C. bookings — reservations with two-stage payment lifecycle.
create table if not exists public.bookings (
  id            bigint generated by default as identity primary key,
  user_id       uuid not null references public.profiles(id) on delete cascade,
  field_id      bigint not null references public.fields(id) on delete cascade,
  booking_date  date not null,
  start_time    time without time zone not null,
  end_time      time without time zone not null,
  price         numeric(10, 2) not null,
  dp_amount     numeric(10, 2) not null,
  status        public.booking_status not null default 'pending',
  created_at    timestamptz not null default timezone('utc'::text, now()),
  updated_at    timestamptz not null default timezone('utc'::text, now())
);

create index if not exists idx_bookings_user_id on public.bookings (user_id);
create index if not exists idx_bookings_field_id on public.bookings (field_id);
create index if not exists idx_bookings_date on public.bookings (booking_date);
create index if not exists idx_bookings_status on public.bookings (status);

-- 2D. payments — receipts for each payment stage (dp / final).
create table if not exists public.payments (
  id            bigint generated by default as identity primary key,
  booking_id    bigint not null references public.bookings(id) on delete cascade,
  amount        numeric(10, 2) not null,
  payment_type  public.payment_type not null,
  receipt_url   text not null,
  status        text not null default 'pending' check (status in ('pending', 'approved', 'rejected')),
  created_at    timestamptz not null default timezone('utc'::text, now()),
  updated_at    timestamptz not null default timezone('utc'::text, now())
);

create index if not exists idx_payments_booking_id on public.payments (booking_id);

