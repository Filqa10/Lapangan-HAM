drop policy if exists "payments_insert_own" on public.payments;
create policy "payments_insert_own" on public.payments
  for insert to authenticated
  with check (
    status = 'pending'
    and amount > 0
    and (select auth.uid())::text = (storage.foldername(receipt_url))[1]
    and exists (
      select 1 from public.bookings b
      where b.id = payments.booking_id
        and b.user_id = (select auth.uid())
        and (
          (payments.payment_type = 'dp' and b.status = 'pending' and payments.amount = b.dp_amount)
          or (payments.payment_type = 'final' and b.status = 'payment_2_pending' and payments.amount = greatest(b.price - b.dp_amount, 0))
        )
    )
  );

-- Combine admin/customer booking UPDATE policies to avoid multiple permissive
-- policy evaluation for the same role/action.
drop policy if exists "bookings_update_admin" on public.bookings;
drop policy if exists "bookings_update_customer_payment_transition" on public.bookings;
create policy "bookings_update_admin_or_customer_payment_transition" on public.bookings
  for update to authenticated
  using (
    public.is_admin()
    or ((select auth.uid()) = user_id and status in ('dp_paid', 'payment_2_pending'))
  )
  with check (
    public.is_admin()
    or ((select auth.uid()) = user_id and status in ('dp_paid', 'payment_2_pending'))
  );

